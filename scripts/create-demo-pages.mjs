function cleanContent(content) {
  // Strip MkDocs badge images
  content = content.replace(
    /^!\[.*?\]\(https:\/\/img\.shields\.io\/.*?\)\s*\n?/gm,
    "",
  );
  // Strip <details open> / </details> / <summary> wrappers
  content = content.replace(/<details[^>]*>\s*\n?/g, "");
  content = content.replace(/<\/details>\s*\n?/g, "");
  content = content.replace(/<summary>.*?<\/summary>\s*\n?/g, "");
  // Strip first H1 (Starlight generates from frontmatter)
  content = content.replace(/^#\s+.+\n\n?/, "");
  // Strip leading empty lines
  content = content.replace(/^\n+/, "");
  return content;
}

function buildFrontmatter(page, order) {
  return `---\ntitle: "${page.title}"\ndescription: "${page.description}"\nsidebar:\n  order: ${order}\n---\n\n`;
}

