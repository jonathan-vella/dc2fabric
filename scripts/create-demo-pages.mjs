// Build reviews page
function buildReviewsPage() {
  const files = [
    "challenge-findings-requirements.json",
    "challenge-findings-architecture-pass1.json",
    "challenge-findings-architecture-pass2.json",
    "challenge-findings-architecture-pass3.json",
    "challenge-findings-cost-estimate.json",
    "challenge-findings-governance-constraints-pass1.json",
    "challenge-findings-governance-revalidation.json",
    "challenge-findings-plan-pass1.json",
    "challenge-findings-plan-pass2.json",
    "challenge-findings-plan-pass3.json",
    "challenge-findings-plan-revalidation.json",
    "challenge-findings-deployment.json",
  ];

  let content = `---
title: "Adversarial Reviews"
description: "Challenger agent findings across all workflow steps"
sidebar:
  order: 9
---

:::caution[Adversarial Review]
The Challenger agent runs rotating-lens reviews at each workflow step — checking for security gaps, governance violations, WAF blind spots, and cost issues. Findings below are actual agent output.
:::

## Review Summary

| Review | Lens | Findings |
|--------|------|----------|
`;

  for (const file of files) {
    const path = `${SRC}/${file}`;
    if (!existsSync(path)) continue;
    try {
      const data = JSON.parse(readFileSync(path, "utf-8"));
      const name = file
        .replace("challenge-findings-", "")
        .replace(".json", "")
        .replace(/-/g, " ");
      const lens = data.lens || data.review_lens || "comprehensive";
      const count = data.findings?.length || data.must_fix?.length || 0;
      const should = data.should_fix?.length || 0;
      content += `| ${name} | ${lens} | ${count} must-fix, ${should} should-fix |\n`;
    } catch {
      // skip malformed
    }
  }

  content += `\n## Detailed Findings\n\n`;

  for (const file of files) {
    const path = `${SRC}/${file}`;
    if (!existsSync(path)) continue;
    try {
      const data = JSON.parse(readFileSync(path, "utf-8"));
      const name = file
        .replace("challenge-findings-", "")
        .replace(".json", "")
        .replace(/-/g, " ");
      content += `\n### ${name}\n\n`;

      const allFindings = [
        ...(data.must_fix || []).map((f) => ({ ...f, severity: "must-fix" })),
        ...(data.should_fix || []).map((f) => ({
          ...f,
          severity: "should-fix",
        })),
        ...(data.findings || []).map((f) => ({
          ...f,
          severity: f.severity || "info",
        })),
      ];

      if (allFindings.length === 0) {
        content += `No findings.\n`;
        continue;
      }

      for (const f of allFindings.slice(0, 5)) {
        const title = f.title || f.finding || f.description || "Finding";
        const sev = f.severity || "info";
        content += `- **[${sev}]** ${title}\n`;
      }
      if (allFindings.length > 5) {
        content += `- *...and ${allFindings.length - 5} more findings*\n`;
      }
    } catch {
      // skip
    }
  }

  return content;
}

// Process all pages
let order = 0;
for (const page of PAGES) {
  let content;

  if (page.custom) {
    if (page.dest === "index.md") content = buildIndexPage();
    else if (page.dest === "03-design.md") content = buildDesignPage();
    else if (page.dest === "08-as-built.md") content = buildAsBuiltPage();
    else if (page.dest === "09-reviews.md") content = buildReviewsPage();
  } else {
    const srcPath = `${SRC}/${page.src}`;
    if (!existsSync(srcPath)) {
      console.warn(`⚠️  Missing: ${srcPath}`);
      order++;
      continue;
    }
    let body = readFileSync(srcPath, "utf-8");
    body = cleanContent(body);
    content = buildFrontmatter(page, order) + body;
  }

  writeFileSync(`${DEST}/${page.dest}`, content);
  console.log(`✅ ${page.dest}`);
  order++;
}

console.log(`\n✅ Done: ${PAGES.length} demo pages created`);
